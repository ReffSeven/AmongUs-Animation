def clear(): 
  if name == 'nt': 
    _ = system('cls')
  else: 
    _ = system('clear')

au_fps = 15
clear()
def runanim(window):
  while(True):
    for n in amongus:
      window.addstr(0, 0, n)
      window.refresh()
      sleep(1/au_fps)

curses.wrapper(runanim)
