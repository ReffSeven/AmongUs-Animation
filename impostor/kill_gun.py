def clear(): 
	if name == 'nt': 
		_ = system('cls')
	else: 
		_ = system('clear')

au_fps = 15
clear()
def runanim(window):
	while(True):
		for n in amongus:
			try:
				window.addstr(0, 0, n)
				window.refresh()
				sleep(1/au_fps)
			except KeyboardInterrupt:
				curses.endwin()
				try: sys.exit(0)
				except SystemExit: os._exit(0)
			except:
				curses.endwin()
			
curses.wrapper(runanim)
